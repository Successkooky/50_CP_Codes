import java.util.*;
public class CodeChef18 {
    public static void main(String args[]){
        try (Scanner input = new Scanner(System.in)) {
            int T=input.nextInt();
   
            for (int i = 0; i < T; i++) {
                int g=input.nextInt();
                int c=input.nextInt();
                int height=(c*c)/(2*g);
                System.out.println(height);

                
}
        }
    }
}

